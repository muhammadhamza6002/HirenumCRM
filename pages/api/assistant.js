export default async function handler(req, res) {
  if (req.method !== "POST") return res.status(405).json({ error: "Method not allowed" });

  const { profileText, clientSlug, source, commentText } = req.body;
  if (!profileText || !clientSlug) {
    return res.status(400).json({ error: "Missing profileText or clientSlug" });
  }

  const groqKey = process.env.GROQ_KEY || process.env.NEXT_PUBLIC_GROQ_KEY;
  const openrouterKey = process.env.OPENROUTER_KEY || process.env.NEXT_PUBLIC_OPENROUTER_KEY;
  if (!groqKey && !openrouterKey) {
    return res.status(500).json({ error: "No API key configured (need GROQ_KEY or OPENROUTER_KEY)" });
  }

  const context = CLIENT_CONTEXT[clientSlug] || CLIENT_CONTEXT.hirenum;

  const systemPrompt = `${context}

PROFILE EXTRACTION RULES (CRITICAL):
You must read the pasted LinkedIn profile text in FULL detail and extract every signal usable for personalization. Pay attention to:
- Current company, role, and title
- Previous employers and roles (even brief mentions)
- Specific equipment, plants, units, processes mentioned in their bullets
- Skills, endorsements, certifications
- Industries and sectors they have worked in
- Regions and geographies
- Any direct quote of their responsibilities (e.g. "Driving cloud adoption strategies", "Handling RO and cooling tower operations")
- Recent activity, posts, or projects mentioned

Use the MOST SPECIFIC technical detail you can find. Generic openers like "saw your work in the industry" are BANNED. The hook MUST cite something concrete from their profile.

EMAIL EXTRACTION:
If the pasted text contains an email address (e.g. from Apollo extension output, or written anywhere), extract it. If no email is present, return an empty string. Do NOT invent or guess emails. Only return what is literally in the text.

You must respond with a JSON object (raw JSON, no markdown, no code fences) with this exact shape:
{
  "name": "Full name extracted from profile",
  "company": "Company name or empty string",
  "role": "Job title or empty string",
  "industry": "Industry guess based on company/role",
  "country": "Country name (full name, e.g. Pakistan, United Arab Emirates, India) inferred from profile location, company location, or city if mentioned. Empty string if truly indeterminate.",
  "email": "Email address if literally present in the pasted text, otherwise empty string",
  "fit": "strong" | "weak" | "not_fit",
  "fit_reason": "One short sentence explaining the fit verdict",
  "score": <integer 0-100>,
  "draft_message": "The personalized DM following the client's rules. Use only the rules above. No hyphens. No exclamation marks."
}`;

  const engagementBlock = source === "post_engagement" && commentText
    ? `\n\nIMPORTANT — POST ENGAGEMENT CONTEXT:\nThis prospect engaged with one of ${clientSlug}'s LinkedIn posts. Here is what they said or engaged on:\n"""\n${commentText}\n"""\n\nIn the DM's personalized hook, reference their comment naturally. Acknowledge what they said and pivot into the relevant business angle. Do NOT say "saw your comment on my post" verbatim — instead, reference the substance of what they said (the topic, the angle, the point they made).`
    : source === "post_engagement"
    ? `\n\nThis prospect engaged with one of ${clientSlug}'s LinkedIn posts (no comment text provided). In the DM, you can mention noticing their engagement briefly, but mostly rely on their profile for personalization.`
    : "";

  const userPrompt = `Analyze this LinkedIn prospect:\n\n"""\n${profileText}\n"""${engagementBlock}`;

  const attempts = [];
  if (groqKey) {
    attempts.push(
      { provider: "groq", model: "llama-3.3-70b-versatile", url: "https://api.groq.com/openai/v1/chat/completions", key: groqKey },
      { provider: "groq", model: "llama-3.1-8b-instant", url: "https://api.groq.com/openai/v1/chat/completions", key: groqKey }
    );
  }
  if (openrouterKey) {
    attempts.push(
      { provider: "openrouter", model: "meta-llama/llama-3.3-70b-instruct:free", url: "https://openrouter.ai/api/v1/chat/completions", key: openrouterKey },
      { provider: "openrouter", model: "deepseek/deepseek-chat-v3-0324:free", url: "https://openrouter.ai/api/v1/chat/completions", key: openrouterKey }
    );
  }

  const errors = [];

  for (const { provider, model, url, key } of attempts) {
    try {
      const headers = {
        "Content-Type": "application/json",
        Authorization: `Bearer ${key}`,
      };
      if (provider === "openrouter") {
        headers["HTTP-Referer"] = "https://hirenum-crm.vercel.app";
        headers["X-Title"] = "Pulse CRM";
      }
      const response = await fetch(url, {
        method: "POST",
        headers,
        body: JSON.stringify({
          model,
          messages: [
            { role: "system", content: systemPrompt },
            { role: "user", content: userPrompt },
          ],
          temperature: 0.6,
        }),
      });

      const data = await response.json();
      if (!response.ok) {
        errors.push(`[${provider}/${model}] ${data.error?.message || "API error"}`);
        continue;
      }

      const text = data.choices?.[0]?.message?.content;
      if (!text) {
        errors.push(`[${provider}/${model}] Empty response`);
        continue;
      }

      let parsed;
      try {
        parsed = JSON.parse(text);
      } catch {
        const match = text.match(/\{[\s\S]*\}/);
        if (!match) {
          errors.push(`[${provider}/${model}] Could not parse JSON`);
          continue;
        }
        try {
          parsed = JSON.parse(match[0]);
        } catch (e) {
          errors.push(`[${provider}/${model}] JSON parse failed: ${e.message}`);
          continue;
        }
      }
      return res.status(200).json({ ...parsed, _model: `${provider}/${model}` });
    } catch (err) {
      errors.push(`[${provider}/${model}] ${err.message}`);
    }
  }

  return res.status(500).json({ error: "All models failed. " + errors.join(" | ") });
}
